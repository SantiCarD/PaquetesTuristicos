    private void listarReservas() {
    DefaultTableModel model = (DefaultTableModel) TablaPA.getModel();
    model.setRowCount(0); 
    
    ArrayList<ReservaEquipamiento> paquetes = sre.getEquipamientos();
    
    for (ReservaEquipamiento paquete : paquetes) {

            model.addRow(new Object[]{
            paquete.getNombre(),
            paquete.getTElementos().toString()
        });
         }
    }

    
